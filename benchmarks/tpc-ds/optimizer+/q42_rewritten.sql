SELECT
    A1.D_YEAR "D_YEAR",
    A2.I_CATEGORY_ID "I_CATEGORY_ID",
    A2.I_CATEGORY "I_CATEGORY",
    SUM(A0.SS_EXT_SALES_PRICE)
FROM
    (
        (
            STORE_SALES A0
            INNER JOIN DATE_DIM A1 ON (A1.D_DATE_SK = A0.SS_SOLD_DATE_SK)
        )
        INNER JOIN ITEM A2 ON (A0.SS_ITEM_SK = A2.I_ITEM_SK)
    )
WHERE
    (A1.D_MOY = 11)
    AND (A1.D_YEAR = 1998)
    AND (A2.I_MANAGER_ID = 1)
GROUP BY
    A2.I_CATEGORY_ID,
    A2.I_CATEGORY,
    A1.D_YEAR
ORDER BY
    4 DESC,
    2 ASC,
    3 ASC
limit
    100;