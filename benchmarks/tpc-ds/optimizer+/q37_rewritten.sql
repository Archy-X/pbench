SELECT
    A2.I_ITEM_ID "I_ITEM_ID",
    A2.I_ITEM_DESC "I_ITEM_DESC",
    A2.I_CURRENT_PRICE "I_CURRENT_PRICE"
FROM
    (
        CATALOG_SALES A0
        INNER JOIN (
            (
                INVENTORY A1
                INNER JOIN ITEM A2 ON (A1.INV_ITEM_SK = A2.I_ITEM_SK)
            )
            INNER JOIN DATE_DIM A3 ON (A3.D_DATE_SK = A1.INV_DATE_SK)
        ) ON (A1.INV_ITEM_SK = A0.CS_ITEM_SK)
    )
WHERE
    (100 <= A1.INV_QUANTITY_ON_HAND)
    AND (A1.INV_QUANTITY_ON_HAND <= 500)
    AND (A2.I_MANUFACT_ID IN (744, 884, 722, 693))
    AND (26 <= A2.I_CURRENT_PRICE)
    AND (A2.I_CURRENT_PRICE <= 56)
    AND (
        A3.D_DATE <= DATE_ADD('day', 90, DATE('2001-06-09'))
    )
    AND (DATE('2001-06-09') <= A3.D_DATE)
GROUP BY
    A2.I_ITEM_ID,
    A2.I_ITEM_DESC,
    A2.I_CURRENT_PRICE
ORDER BY
    1 ASC
limit
    100;