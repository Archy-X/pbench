SELECT
    A2.D_YEAR "D_YEAR",
    A1.I_BRAND_ID "BRAND_ID",
    A1.I_BRAND "BRAND",
    SUM(A0.SS_NET_PROFIT) "SUM_AGG"
FROM
    (
        (
            STORE_SALES A0
            INNER JOIN ITEM A1 ON (A0.SS_ITEM_SK = A1.I_ITEM_SK)
        )
        INNER JOIN DATE_DIM A2 ON (A2.D_DATE_SK = A0.SS_SOLD_DATE_SK)
    )
WHERE
    (A1.I_MANUFACT_ID = 445)
    AND (A2.D_MOY = 12)
GROUP BY
    A2.D_YEAR,
    A1.I_BRAND,
    A1.I_BRAND_ID
ORDER BY
    1 ASC,
    4 DESC,
    2 ASC
limit
    100;