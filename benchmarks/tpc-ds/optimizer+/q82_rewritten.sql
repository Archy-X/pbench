SELECT
    A2.I_ITEM_ID "I_ITEM_ID",
    A2.I_ITEM_DESC "I_ITEM_DESC",
    A2.I_CURRENT_PRICE "I_CURRENT_PRICE"
FROM
    (
        STORE_SALES A0
        INNER JOIN (
            (
                INVENTORY A1
                INNER JOIN ITEM A2 ON (A1.INV_ITEM_SK = A2.I_ITEM_SK)
            )
            INNER JOIN DATE_DIM A3 ON (A3.D_DATE_SK = A1.INV_DATE_SK)
        ) ON (A1.INV_ITEM_SK = A0.SS_ITEM_SK)
    )
WHERE
    (100 <= A1.INV_QUANTITY_ON_HAND)
    AND (A1.INV_QUANTITY_ON_HAND <= 500)
    AND (A2.I_MANUFACT_ID IN (105, 513, 180, 137))
    AND (69 <= A2.I_CURRENT_PRICE)
    AND (A2.I_CURRENT_PRICE <= 99)
    AND (
        A3.D_DATE <= DATE_ADD('day', 60, DATE('1998-06-06'))
    )
    AND (DATE('1998-06-06') <= A3.D_DATE)
GROUP BY
    A2.I_ITEM_ID,
    A2.I_ITEM_DESC,
    A2.I_CURRENT_PRICE
ORDER BY
    1 ASC
limit
    100;